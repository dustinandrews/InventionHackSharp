		private static void IterateMaps(MapFeatureDetector detector, int[,] conv)
		{
			var min = conv.Min();
			var max = conv.Max();
			for (int i = min; i <= max; i++)
			{
				var splats = detector.FilterArray(conv, i);
				if(splats.Total() > 0)
				{
					Debug.WriteLine(i);
					Debug.WriteLine(detector.ToMapString(splats));
				}
			}
		}


